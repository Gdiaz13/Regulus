create table if not exists model_accuracy_results (
    id integer generated by default as identity primary key,
    prediction_id integer not null references predictions (id) on delete cascade,
    user_id uuid not null,
    asset_id varchar(64) not null,
    asset_type varchar(32) not null,
    model_name varchar(64) not null,
    model_version varchar(64) not null,
    predicted_percent_change double precision not null,
    actual_percent_change double precision not null,
    absolute_percent_error double precision not null,
    direction_matched boolean not null,
    actual_price numeric(18, 4) not null,
    target_date date not null,
    actual_date date not null,
    is_mock boolean not null default false,
    predicted_on timestamptz not null,
    scored_at timestamptz not null default now()
);

create unique index if not exists ux_model_accuracy_results_prediction
    on model_accuracy_results (prediction_id);
create index if not exists ix_model_accuracy_results_model
    on model_accuracy_results (model_name, scored_at desc);
create index if not exists ix_model_accuracy_results_user
    on model_accuracy_results (user_id, scored_at desc);
